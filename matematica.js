let calculo = {
    soma : function(convA, convB){
        return convA+convB;
    },
    mult : function(convA, convB){
        return convA*convB;
    },
    sub : function(convA, convB){
        return convA-convB;
    },
    div : function(convA, convB){
        return convA/convB;
    }
};

module.exports = calculo;